package sample;

public class Service {
    private String id;
    private String dateCreation;
    private String ls;
    private String service;
    private String servicetype1;
    private String servicetype2;
    private String status;
    private String tech;
    private String troubles;
    private String dateClose;
    private String troublestype;

    public Service() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDateCreation() {
        return dateCreation;
    }

    public void setDateCreation(String dateCreation) {
        this.dateCreation = dateCreation;
    }

    public String getLs() {
        return ls;
    }

    public void setLs(String ls) {
        this.ls = ls;
    }

    public String getService() {
        return service;
    }

    public void setService(String service) {
        this.service = service;
    }

    public String getServicetype1() {
        return servicetype1;
    }

    public void setServicetype1(String servicetype1) {
        this.servicetype1 = servicetype1;
    }

    public String getServicetype2() {
        return servicetype2;
    }

    public void setServicetype2(String servicetype2) {
        this.servicetype2 = servicetype2;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getTech() {
        return tech;
    }

    public void setTech(String tech) {
        this.tech = tech;
    }

    public String getTroubles() {
        return troubles;
    }

    public void setTroubles(String troubles) {
        this.troubles = troubles;
    }

    public String getDateClose() {
        return dateClose;
    }

    public void setDateClose(String dateClose) {
        this.dateClose = dateClose;
    }

    public String getTroublestype() {
        return troublestype;
    }

    public void setTroublestype(String troublestype) {
        this.troublestype = troublestype;
    }
}
