package sample;

public class OMC_Tech {
    private String id;
    private String name;
    private String rate;
    private String coeffFading;
    private String DTT;
    private String place;

    public OMC_Tech() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRate() {
        return rate;
    }

    public void setRate(String rate) {
        this.rate = rate;
    }

    public String getCoeffFading() {
        return coeffFading;
    }

    public void setCoeffFading(String coeffFading) {
        this.coeffFading = coeffFading;
    }

    public String getDTT() {
        return DTT;
    }

    public void setDTT(String DTT) {
        this.DTT = DTT;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }
}
