package sample;

public class OA_Tech {
    private String id;
    private String name;
    private String ports;
    private String DTS;
    private String DTSpeed;
    private String place;

    public OA_Tech() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPorts() {
        return ports;
    }

    public void setPorts(String ports) {
        this.ports = ports;
    }

    public String getDTS() {
        return DTS;
    }

    public void setDTS(String DTS) {
        this.DTS = DTS;
    }

    public String getDTSpeed() {
        return DTSpeed;
    }

    public void setDTSpeed(String DTSpeed) {
        this.DTSpeed = DTSpeed;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }
}
