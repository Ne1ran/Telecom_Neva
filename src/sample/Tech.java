package sample;

public class Tech {
    private String Omc;
    private String Osd;
    private String Oa;

    public Tech() {
    }

    public String getOmc() {
        return Omc;
    }

    public void setOmc(String Omc) {
        this.Omc = Omc;
    }

    public String getOsd() {
        return Osd;
    }

    public void setOsd(String Osd) {
        this.Osd = Osd;
    }

    public String getOa() {
        return Oa;
    }

    public void setOa(String Oa) {
        this.Oa = Oa;
    }
}
