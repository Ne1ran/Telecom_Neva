package sample;

public class Tech {
    private String ID;
    private String OMC;
    private String OSD;
    private String OA;

    public Tech() {
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getOMC() {
        return OMC;
    }

    public void setOMC(String OMC) {
        this.OMC = OMC;
    }

    public String getOSD() {
        return OSD;
    }

    public void setOSD(String OSD) {
        this.OSD = OSD;
    }

    public String getOA() {
        return OA;
    }

    public void setOA(String OA) {
        this.OA = OA;
    }
}
